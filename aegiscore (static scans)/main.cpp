void startEvent() {
    hEvent = CreateEventA(
        NULL,               
        TRUE,               
        FALSE,              
        "Global\\hooking" 
    );

    if (hEvent == NULL) {
        std::cout << "Error creating event: " << GetLastError() << std::endl;
    }
}

void stopEvent() {
    SetEvent(hEvent);
    std::cout << "Signal sent!" << std::endl;
    CloseHandle(hEvent);
}


